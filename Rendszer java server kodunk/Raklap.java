class Raklap
{
	private String name;
	private String goods;
	private int quantity;

	public Raklap(String name, String goods, int quantity)
	{
		this.name = name;
		this.goods = goods;
		this.quantity = quantity;
	}

	public String getName()
	{
		return name;
	}

	public String getGoods()
	{
		return goods;
	}

	public int getQuantity()
	{
		return quantity;
	}
}
